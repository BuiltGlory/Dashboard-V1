export function parseSellAskingPrice(askingPrice: string): number {
  const n = parseInt(askingPrice.replace(/[^\d]/g, ''), 10)
  return Number.isFinite(n) ? n : 0
}

function enrichSellRequest(r: SellRequest, index: number): SellRequest {
  const seed = index * 17 + r.id.length
  return {
    ...r,
    isDraft: r.isDraft ?? false,
    draftStep: r.draftStep ?? null,
    draftSavedAt: r.draftSavedAt ?? null,
    views: r.views ?? 20 + (seed % 480),
    enquiryCount: r.enquiryCount ?? seed % 21,
    visitCount: r.visitCount ?? seed % 11,
    saveCount: r.saveCount ?? seed % 51,
    viewsThisWeek: r.viewsThisWeek ?? [12, 8, 15, 22, 18, 9, 14],
    salePrice: r.salePrice ?? null,
    saleDate: r.saleDate ?? null,
    saleBuyerName: r.saleBuyerName ?? null,
    pauseReason: r.pauseReason ?? null,
  }
}

