export function getCompletenessColor(percent: number) {
  if (percent < 50) return 'bg-red-500'
  if (percent < 80) return 'bg-orange-500'
  return 'bg-green-500'
}

export function getMissingItems(request: SellRequest): string[] {
  const missing: string[] = []
  if (request.photosCount === 0) missing.push('Photos')
  const missingDocs = request.documents.filter((d) => d.status === 'missing').length
  if (missingDocs > 0) missing.push(`${missingDocs} document(s)`)
  if (!request.description?.trim()) missing.push('Description')
  if (!request.specifications.bhk && !request.specifications.plotArea) missing.push('Specifications')
  return missing
}

export function getSimilarProperties(request: SellRequest): SimilarProperty[] {
  return MOCK_SIMILAR_PROPERTIES.filter(
    (p) => p.propertyType === request.propertyType && p.city === request.city,
  ).slice(0, 3)
}
